# ─── ACCESS ───────────────────────────────────────────────────────────────────

def all_layers() -> List[LayerSpec]:
    """Every registered layer (core + custom), regardless of enabled state."""
    return CORE_LAYERS + CUSTOM_LAYERS


def core_keys() -> List[str]:
    """Keys of the core layers. An event missing one of these is incomplete and
    is excluded from the catalog; missing a custom layer only is tolerated."""
    return [spec.key for spec in CORE_LAYERS]


def layer_by_key(key: str) -> Optional[LayerSpec]:
    for spec in all_layers():
        if spec.key == key:
            return spec
    return None
